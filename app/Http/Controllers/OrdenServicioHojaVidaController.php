    {
        try {
            $hojas_vida = $request->all();
            foreach ($hojas_vida as $hoja_vida) {
                $result = new OrdenServicioHojaVida;
                $result->candidato_id = $hoja_vida->candidato_id;
                $result->ruta_archivo = $hoja_vida->ruta_archivo;
                $result->save();
            }
            return true;
        } catch (\Exception $e) {
            //throw $th;
            return false;
        }
    }

