    {
        $result = Banco::where('cod_ban', '07')
            ->orWhere('cod_ban', '01')
            ->orWhere('cod_ban', '52')
            ->orWhere('cod_ban', '13')
            ->select(
                'cod_ban as id',
                'nom_ban as nombre',
            )
            ->get();
        return response()->json($result);
